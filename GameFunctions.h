//cursor
void SetCursorPosition(short xPos, short yPos)
{
	HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE); // визначається вікно консолі у якій буде мінятись колір
	COORD position = { xPos, yPos };
	SetConsoleCursorPosition(hConsole, position);
}

// color

void PrintColorText(char str[], WORD color = FOREGROUND_GREEN | FOREGROUND_INTENSITY)
{
	HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE); // визначається вікно консолі у якій буде мінятись колір
	CONSOLE_SCREEN_BUFFER_INFO csbi; // структура для збереження поточного стану консолі
	GetConsoleScreenBufferInfo(hConsole, &csbi); // зберігається попередній стан атрибутів/властивостей консолі

	SetConsoleTextAttribute(hConsole, color); // задається колір тексту 

	cout << str; // текст надрукується червоним

	SetConsoleTextAttribute(hConsole, csbi.wAttributes); // відновлюється попередній колір тексту
}


